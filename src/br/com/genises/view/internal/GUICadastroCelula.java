    private void verificaExistencia() throws Exception {

        BCelula bCelulaR = new BCelula();
        Celula celulaV = new Celula(); //celula editada
        celulaV.setNome(tfNome.getText());

        if (bCelulaR.verificaExistencia(celulaV) == true) {
            tfNome.requestFocus();
            throw new Exception("O NOME INFORMADO JÁ EXISTE CADASTRADO!");

        }
    }
