    private void carregarCidades(List<Cidade> cidades) {
        cbCidade.removeAllItems();
        cbCidade.addItem("Selecione...");
        try {
            for (int i = 0; i < cidades.size(); i++) {

                cbCidade.addItem(cidades.get(i).getCidade());
                cbCidade.setSelectedIndex(0);
            }
        } catch (Exception ex) {
            Logger.getLogger(GUICadastroIgreja.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void verificaCampos() throws Exception {
        if (tfNome.getText().trim().equals("")) {
            tfNome.requestFocus();
            throw new Exception("DIGITE O NOME!");
        }
        if (tfEndereco.getText().trim().equals("")) {
            tfEndereco.requestFocus();
            throw new Exception("DIGITE O ENDEREÇO!");
        }
        if (tfBairro.getText().trim().equals("")) {
            tfBairro.requestFocus();
            throw new Exception("DIGITE O BAIRRO!");
        }
        if (tfCep.getText().trim().equals("")) {
            tfCep.requestFocus();
            throw new Exception("DIGITE O CEP!");
        }
        if (cbUf.getSelectedItem().equals("Selecione...")) {
            cbUf.requestFocus();
            throw new Exception("SELECIONE UM ESTADO!");
        }
        if (cbCidade.getSelectedItem().equals("Selecione...")) {
            cbCidade.requestFocus();
            throw new Exception("SELECIONE UMA CIDADE!");
        }
        if (membroAnfitriao.getId() == 0) {
            tfAnfitriao.requestFocus();
            throw new Exception("INFORME O NOME DO ANFITRIÃO!");
        }
        if (lider.getId() == 0) {
            tfLider.requestFocus();
            throw new Exception("INFORME O NOME DO LIDER!");
        }
        if (liderTreinamento.getId() == 0) {
            tfLiderTreinamento.requestFocus();
            throw new Exception("INFORME O NOME DO LIDER EM TREINAMENTO!");
        }
        if (supervisor.getId() == 0) {
            tfSupervisor.requestFocus();
            throw new Exception("INFORME O NOME DO SUPERVISOR!");
        }
        if (tfHorario.getText().trim().equals(":")) {
            tfHorario.requestFocus();
            throw new Exception("DIGITE O HORÁRIO!");
        }

    }

    private void gravarDados() throws Exception {
        Celula c = new Celula();
        BCelula bCelula = new BCelula();
        Cidade cidade = new Cidade();
        Membro anfitriao = new Membro();

        c.setNome(tfNome.getText());
        c.setEndereco(tfNome.getText());
        c.setBairro(tfBairro.getText());
        c.setCep(tfCep.getText());
        cidade.setUf((String) cbUf.getSelectedItem());
        cidade.setCidade((String) cbCidade.getSelectedItem());
        c.setCidade(cidade);

        c.setAnfitriao(membroAnfitriao);
        c.setLider(lider);
        c.setLiderTreinamemto(liderTreinamento);
        c.setSupervisor(supervisor);

        c.setDia((String) cbDia.getSelectedItem());
        c.setHora(tfHorario.getText());

        bCelula.gravar(c);
        JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!");

    }

