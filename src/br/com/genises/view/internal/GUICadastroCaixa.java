    private void bGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGravarActionPerformed
        try {
            if (tfDocumento.getText().trim().equals("")) {
                throw new NegocioException("Digite o numero do lançamento!");
            }
            if (tfData.getText().trim().equals("/  /")) {
                throw new NegocioException("Digite a data do lançamento!");
            }
            if (tfHora.getText().trim().equals(":  :")) {
                throw new NegocioException("Digite a hora do lançamento!");
            }
            if (tfHistorico.getText().trim().equals("")) {
                throw new NegocioException("Digite o historico do lançamento!");
            }
            if (tfValor.getText().trim().equals("")) {
                throw new NegocioException("Digite o valor do lançamento!");
            }
            try {
                BigDecimal a = new BigDecimal(tfValor.getText().replaceAll(",", "."));
            } catch (Exception e) {
                throw new Exception("digite um valor correto! ");
            }
            Caixa caixa = new Caixa();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy,hh:mm:ss");
            if (!cbPlanoConta.getSelectedItem().toString().trim().equals("Selecione...")) {

                caixa.setPlano((PlanoConta) cbPlanoConta.getSelectedItem());
            }
            caixa.setDataHora(LocalDate.parse(tfData.getText() + "," + tfHora.getText(), formatter));
            caixa.setDocumento(tfDocumento.getText());
            caixa.setHistorico(tfHistorico.getText());
            caixa.setValor(new BigDecimal(tfValor.getText().replaceAll(",", ".")));

            DaoCaixa daoCaixa = new DaoCaixa();
            daoCaixa.salvar(caixa);
            JOptionPaneUtil.messageInformation("CADASTRADO COM SUCESSO!");

            limpar();
        } catch (NegocioException e) {
            JOptionPaneUtil.messageInformation(e.getMessage());
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPaneUtil.messageError(e.getMessage());
        }
    }//GEN-LAST:event_bGravarActionPerformed


